public class self_homework6 {
    public static void main(String[] args){
        //二进制：由0，1组成，代码中以0b开头
        //十进制：由0-9组成，前面不加任何前缀
        //八进制：由0-7组成，代码中以0开头
        //十六进制：由0-9和a-f组成，代码中以0x开头

        System.out.println(17); //十进制
        System.out.println(017); //八进制
        //System.out.println(0b123); //语法错误
        System.out.println(0x123); //十六进制
    }
}
