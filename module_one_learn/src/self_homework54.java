public class self_homework54 {
    public static void main(String[] args) {
        // 带参数的方法的定义和调用

        System.out.println("-------------------");


        // 调用
        // 单个参数
        /*
        * 方法名（参数）;
        * 范例1.:method（10）;
        * 范例2.method（变量）;*/

        // 多个参数
        /*
        * 方法名（参数1，参数2，...）;
        * 范例1.getSum（10，20）;
        * 范例2.getSum（变量1，变量2）;
        *
        * 注意：方法调用时，参数的数量与类型必须与方法定义中小括号里面的变量
        * 一一对应，否则程序将报错*/

    }
    // 格式：  单个参数
     /*   public static void 方法名（参数）{
     ...
    }*/

        // 范例：
       /* public static void method(int number){
            ...
        }*/



    // 格式： 多个参数
    /*   public static void 方法名（参数1,参数2）{
     ...
    }*/

    // 范例：
       /* public static void method(int number1,int number2){
            ...
        }*/
}
