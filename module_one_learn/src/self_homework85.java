public class self_homework85 {
    public static void main(String[] args){
        // 基本数据类型和引用数据类型

        /*
        * 基本数据类型：
        *   在变量当中存储的是真实的数据值
        *   是自己玩自己的，跟其他的空间没有任何的关系
        *   数据值存储在自己的空间里面
        *   特点：赋值给其他变量，也是赋的真实值*/


        /*引用数据类型:
        *   引用引用，就是把别人的东西拿过来用
        *   把别的空间的东西拿过来用
        *   数据值是存储在其他空间中，自己的空间中存储的是地址值
        *   特点：赋值给其他变量，赋的地址值
        * */

        System.out.println("基本数据类型和引用数据类型");
    }
}
